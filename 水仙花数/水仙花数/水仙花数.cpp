#include <iostream>
using namespace std;
int main()
{
	int i;                             //定义循坏变量
	cout << "水仙花数有：" << endl;    //提示信息
	for (i = 100; i <= 999; i++)       //从所有三位数当中找
		if ((i % 10) * (i % 10) * (i % 10) +\
			(i / 10 % 10) * (i / 10 % 10) * (i / 10 % 10) + \
			(i / 100) * (i / 100) * (i / 100) == i)            //换行把他的个位十位百位三次方并判断是否等于原数
			cout << i<<endl;        //原数则输出，该数为水仙花数
	return 0;
}
