#include <iostream>  //预处理命令
using namespace std; //名称空间
int main()
{
    cout << "       过故人庄" << endl;          //诗名
    cout << "        孟浩然" << endl;           //作者
    cout << "故人具鸡黍，邀我至田家。" << endl; //首联 
    cout << "绿树村边合，青山郭外斜。" << endl; //颔联
    cout << "开轩面场圃，把酒话桑麻。" << endl; //颈联
    cout << "待到重阳日，还来就菊花。" << endl; //尾联
    return 0;  //结束运行
}
